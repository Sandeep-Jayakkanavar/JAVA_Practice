package com.learning.java;

public class _21_CS_Loop_FOR_Break {

    public static void main(String[] args) {

        for (int i=1;i<=5;i++){
            System.out.print(i+", "); // 1, 2, 3,
            if(i == 3){
                break; // Come out of the FOR loop when value of variable 'i' is 3
            }
        }

    }// main
}// class





