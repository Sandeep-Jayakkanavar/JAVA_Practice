package com.learning.java;

public class _18_ControlStructure_IfElse {

    public static void main(String[] args) {
        int number1 = 10;

        if (number1>5){
            System.out.println("number1: "+number1+" is greater than 5");
        }else if (number1 ==5 ){
            System.out.println("number1: "+number1+" is equal to 5");
        }else {
            System.out.println("number1: "+number1+" is lesser than 5");
        }

    }// main
}// class





