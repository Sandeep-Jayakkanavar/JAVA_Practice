package com.learning.java;

public class _26_CS_SwitchCase {

    public static void main(String[] args) {

      String str = "a";

      switch (str){
          case "a":
              System.out.println("Value is a"); // Value is a
              break;
          case "b":
              System.out.println("Value is b");
              break;
          default:
              System.out.println("Option not-in-scope");
      }
    }// main
}// class





